# Cell 5: Dataset
class BPRDataset(Dataset):
    def __init__(self, users, items, user_items, n_items, neg=8):
        self.users, self.items, self.user_items = users, items, user_items
        self.n_items, self.neg = n_items, neg
    def __len__(self): return len(self.users)
    def __getitem__(self, idx):
        u, pos = self.users[idx], self.items[idx]
        negs = []
        while len(negs) < self.neg:
            n = np.random.randint(0, self.n_items)
            if n not in self.user_items[u]: negs.append(n)
        return u, pos, negs

def collate(batch):
    return torch.LongTensor([b[0] for b in batch]), torch.LongTensor([b[1] for b in batch]), torch.LongTensor([b[2] for b in batch])

# Cell 6: CL4Rec Model
class DomainMasking(nn.Module):
    def __init__(self, n_domains, dim):
        super().__init__()
        self.user_masks = nn.Parameter(torch.randn(n_domains, dim)*0.1 + 0.5)
        self.item_masks = nn.Parameter(torch.randn(n_domains, dim)*0.1 + 0.5)
    def get_mask(self, d, t='user'): return torch.sigmoid(self.user_masks[d] if t=='user' else self.item_masks[d])
    def apply_mask(self, e, d, t='user'): return e * self.get_mask(d, t).unsqueeze(0)

class CL4Rec(nn.Module):
    def __init__(self, n_users, n_items, n_domains, dim=128, hidden=[256,128], drop=0.2):
        super().__init__()
        self.n_users, self.n_items, self.n_domains, self.dim = n_users, n_items, n_domains, dim
        self.user_emb = nn.Embedding(n_users, dim)
        self.item_emb = nn.Embedding(n_items, dim)
        nn.init.xavier_uniform_(self.user_emb.weight)
        nn.init.xavier_uniform_(self.item_emb.weight)
        self.mask = DomainMasking(n_domains, dim)
        self.u_mlp = nn.Sequential(nn.Linear(dim,hidden[0]),nn.ReLU(),nn.Dropout(drop),
                                   nn.Linear(hidden[0],hidden[1]),nn.ReLU(),nn.Dropout(drop),nn.Linear(hidden[1],dim))
        self.i_mlp = nn.Sequential(nn.Linear(dim,hidden[0]),nn.ReLU(),nn.Dropout(drop),
                                   nn.Linear(hidden[0],hidden[1]),nn.ReLU(),nn.Dropout(drop),nn.Linear(hidden[1],dim))
        self.cur_domain = 0

    def get_emb(self, uids, iids, d, train=True):
        ue, ie = self.user_emb(uids), self.item_emb(iids)
        if train:
            ue = self.mask.apply_mask(ue, d, 'user')
            ie = self.mask.apply_mask(ie, d, 'item')
        else:
            um = torch.stack([self.mask.get_mask(i,'user') for i in range(self.n_domains)]).max(0)[0]
            im = torch.stack([self.mask.get_mask(i,'item') for i in range(self.n_domains)]).max(0)[0]
            ue, ie = ue * um.unsqueeze(0), ie * im.unsqueeze(0)
        return ue + self.u_mlp(ue), ie + self.i_mlp(ie)

    def forward(self, u, pos, neg, d, train=True):
        ue, pe = self.get_emb(u, pos, d, train)
        _, ne = self.get_emb(u.repeat_interleave(neg.size(1)), neg.view(-1), d, train)
        ne = ne.view(u.size(0), neg.size(1), -1)
        ps = (ue * pe).sum(-1)
        ns = (ue.unsqueeze(1) * ne).sum(-1).mean(1)
        loss = -F.logsigmoid(ps - ns + 0.5).mean()
        reg = 0.001 * (ue.norm(2)**2 + pe.norm(2)**2 + ne.norm(2)**2) / u.size(0)
        return loss + reg

    def predict(self, uids, d):
        with torch.no_grad():
            ue = self.user_emb(uids)
            um = torch.stack([self.mask.get_mask(i,'user') for i in range(self.n_domains)]).max(0)[0]
            ue = (ue * um.unsqueeze(0)) + self.u_mlp(ue * um.unsqueeze(0))
            ie = self.item_emb.weight
            im = torch.stack([self.mask.get_mask(i,'item') for i in range(self.n_domains)]).max(0)[0]
            ie = (ie * im.unsqueeze(0)) + self.i_mlp(ie * im.unsqueeze(0))
            return torch.mm(ue, ie.t())

    def grad_mask(self, d):
        if d == 0: return None, None
        um = 1 - torch.stack([self.mask.get_mask(i,'user') for i in range(d)]).max(0)[0]
        im = 1 - torch.stack([self.mask.get_mask(i,'item') for i in range(d)]).max(0)[0]
        return um, im

# Cell 7: Training & Evaluation Functions
def train_domain(model, loader, d, opt, sched, epochs=40):
    model.train()
    losses = []
    for ep in tqdm(range(epochs), desc=f"Domain {d}"):
        total = 0
        for u, pos, neg in loader:
            u, pos, neg = u.to(device), pos.to(device), neg.to(device)
            opt.zero_grad()
            loss = model(u, pos, neg, d, True)
            loss.backward()
            um, im = model.grad_mask(d)
            if um is not None:
                if model.user_emb.weight.grad is not None: model.user_emb.weight.grad *= um.unsqueeze(0)
                if model.item_emb.weight.grad is not None: model.item_emb.weight.grad *= im.unsqueeze(0)
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            opt.step()
            total += loss.item()
        sched.step()
        losses.append(total/len(loader))
    return losses

def evaluate(model, test_d, train_ui, d, k=50, filt=None):
    model.eval()
    users = list(test_d['user_test_item'].keys())
    if filt: users = [u for u in users if u in filt]
    if not users: return 0,0,0
    h10, h20, h50, tot = 0, 0, 0, 0
    for i in range(0, len(users), 512):
        batch = users[i:i+512]
        uids = torch.LongTensor(batch).to(device)
        scores = model.predict(uids, d)
        for j, u in enumerate(batch):
            if u in train_ui:
                for it in train_ui[u]: scores[j, it] = -float('inf')
        _, topk = torch.topk(scores, k, 1)
        topk = topk.cpu().numpy()
        for j, u in enumerate(batch):
            ti = test_d['user_test_item'][u]
            if ti in topk[j]: h50 += 1
            if ti in topk[j,:20]: h20 += 1
            if ti in topk[j,:10]: h10 += 1
            tot += 1
    return h10/tot*100, h20/tot*100, h50/tot*100

