class Baseline(nn.Module):
    def __init__(self, nu, ni, dim, hid, drop):
        super().__init__()
        self.ue = nn.Embedding(nu, dim); self.ie = nn.Embedding(ni, dim)
        nn.init.xavier_uniform_(self.ue.weight); nn.init.xavier_uniform_(self.ie.weight)
        self.umlp = nn.Sequential(nn.Linear(dim,hid[0]),nn.ReLU(),nn.Dropout(drop),nn.Linear(hid[0],hid[1]),nn.ReLU(),nn.Dropout(drop),nn.Linear(hid[1],dim))
        self.imlp = nn.Sequential(nn.Linear(dim,hid[0]),nn.ReLU(),nn.Dropout(drop),nn.Linear(hid[0],hid[1]),nn.ReLU(),nn.Dropout(drop),nn.Linear(hid[1],dim))
        self.ni = ni
    def forward(self, u, pos, neg):
        ue = self.ue(u) + self.umlp(self.ue(u))
        pe = self.ie(pos) + self.imlp(self.ie(pos))
        ne = self.ie(neg); ne = ne + self.imlp(ne.view(-1, ne.size(-1))).view(ne.size())
        ps, ns = (ue*pe).sum(-1), (ue.unsqueeze(1)*ne).sum(-1).mean(1)
        return -F.logsigmoid(ps - ns + 0.5).mean() + 0.001*(ue.norm(2)**2+pe.norm(2)**2+ne.norm(2)**2)/u.size(0)
    def predict(self, u):
        with torch.no_grad():
            ue = self.ue(u) + self.umlp(self.ue(u))
            ie = self.ie.weight + self.imlp(self.ie.weight)
            return torch.mm(ue, ie.t())

